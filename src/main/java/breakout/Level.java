class Level2 extends Level {
    public Level2(int levelNumber) {
        super(levelNumber);
    }

    @Override
    public void setUpBlocks(Group root) {
        // Create and add blocks
        for (int row = 0; row < 23; row++) {
            for (int col = 0; col < 33; col++) {
                if (duke[row][col] == 1) {
                    int x = col * (blockWidth + 5);
                    int y = row * (blockHeight + 5);
                    int health = 1;
                    Block block = new NormalBlock(x, y, blockWidth + 5, blockHeight + 5, health);
                    root.getChildren().add(block);
                }
                if (duke[row][col] == 2) {
                    int x = col * (blockWidth + 5);
                    int y = row * (blockHeight + 5);
                    int health = 1;
                    Block block = new BadBlock(x, y, blockWidth + 5, blockHeight + 5, health);
                    root.getChildren().add(block);
                }
                if (duke[row][col] == 3) {
                    int x = col * (blockWidth + 5);
                    int y = row * (blockHeight + 5);
                    int health = 9999;
                    Block block = new UnbreakableBlock(x, y, blockWidth + 5, blockHeight + 5, health);
                    root.getChildren().add(block);
                }

            }
        }
    }
}

